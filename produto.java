public class produto {
     
    private int idProduto;
    private String nomeProduto;
    private int valorProduto;
    private int tamanhoProduto;
    private int corProduto;
    private int generoProduto;
    private int quantidadeProduto;
    
    public produto(){
        
    }

    public int getIdProduto() {
        return idProduto;
    }

    public void setIdProduto(int idProduto) {
        this.idProduto = idProduto;
    }

    public String getNomeProduto() {
        return nomeProduto;
    }

    public void setNomeProduto(String nomeProduto) {
        this.nomeProduto = nomeProduto;
    }

    public int getValorProduto() {
        return valorProduto;
    }

    public void setValorProduto(int valorProduto) {
        this.valorProduto = valorProduto;
    }

    public int getTamanhoProduto() {
        return tamanhoProduto;
    }

    public void setTamanhoProduto(int tamanhoProduto) {
        this.tamanhoProduto = tamanhoProduto;
    }

    public int getCorProduto() {
        return corProduto;
    }

    public void setCorProduto(int corProduto) {
        this.corProduto = corProduto;
    }

    public int getGeneroProduto() {
        return generoProduto;
    }

    public void setGeneroProduto(int generoProduto) {
        this.generoProduto = generoProduto;
    }

    public int getQuantidadeProduto() {
        return quantidadeProduto;
    }

    public void setQuantidadeProduto(int quantidadeProduto) {
        this.quantidadeProduto = quantidadeProduto;
    }
    
    
    
    
}
